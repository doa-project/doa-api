package org.example.doaapiproject.models;

import io.swagger.v3.oas.annotations.media.Schema;
import jakarta.validation.constraints.Email;
import jakarta.validation.constraints.Min;
import jakarta.validation.constraints.NotNull;
import jakarta.validation.constraints.Size;
import org.springframework.data.annotation.Id;
import org.springframework.data.mongodb.core.mapping.Document;

import java.util.StringJoiner;

@Document("user")
@Schema(description = "Represents an user in the system")
public class User {
    @Id
    @Schema(description = "Represents the automatic id generated by mongo", example = "f51bb4362e")
    private String id;

    @Min(value = 0, message = "the id must be bigger then or equal to zero")
    @Schema(description = "Represent the id of the user", example = "0")
    private Integer userId;
    @NotNull(message = "name cannot be null")
    @Size(max = 50, message = "the number of characters in the name must be between 0 and 50")
    @Schema(description = "Represents the name of the user", example = "User")
    private String name;
    @NotNull(message = "e-mail cannot be null")
    @Email(message = "invalid e-mail")
    @Size(max = 100, message = "the number of characters in the e-mail must be between 0 and 100")
    @Schema(description = "Represents the e-mail of the user", example = "user.24@gmail.com")
    private String email;
    @Schema(description = "Represents the photo of the user", example = "https://firebasestorage.googleapis.com/v0/b/doa-app-447b2.appspot.com/o/doaUser.png?alt=media&token=c17080a3-7af1-4bba-931b-6efa18cf9e44")
    private String userPhoto;

    // construtores
    public User(){}

    public User(String id, Integer userId, String name, String email, String userPhoto) {
        this.id = id;
        this.userId = userId;
        this.name = name;
        this.email = email;
        this.userPhoto = userPhoto;
    }

    public User(String name, String email, String userPhoto) {
        this.name = name;
        this.email = email;
        this.userPhoto = userPhoto;
    }

    // getters e setters

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    public String getUserPhoto() {
        return userPhoto;
    }
    public void setUserPhoto(String userPhoto) {
        this.userPhoto = userPhoto;
    }

    // toString
    public String toString() {
        return "User{\n id=" + this.id +
                "\n ,userId=" + this.userId +
                "\n ,name='" + this.name +
                "'\n ,email='" + this.email +
                "'\n ,userPhoto='" + this.userPhoto +
                "'\n}";
    }
}
